logging.basicConfig(
    format="%(asctime)s | %(levelname)s | %(message)s",
    level=logging.INFO
)
logger = logging.getLogger(__name__)


def build_keyboard():
    buttons, row = [], []
    for key, data in MOODS.items():
        row.append(InlineKeyboardButton(data["label"], callback_data=f"mood_{key}"))
        if len(row) == 2:
            buttons.append(row)
            row = []
    if row:
        buttons.append(row)
    return InlineKeyboardMarkup(buttons)


def build_response(mood: dict) -> str:
    msg        = random.choice(mood["messages"])
    compliment = random.choice(mood["compliments"])
    tip        = random.choice(mood["tips"])
    line       = "─" * 30
    return (
        f"{mood['emoji']}  {mood['header']}\n"
        f"{line}\n\n"
        f"{msg}\n\n"
        f"{line}\n"
        f"{compliment}\n\n"
        f"{tip}\n"
        f"{line}\n"
        f"                             {YOUR_NAME} ❤️"
    )


async def start(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text(
        "Привет 💙\n\nКак ты сейчас себя чувствуешь?\nВыбери настроение — и я отвечу ✨",
        reply_markup=build_keyboard()
    )


async def menu_cmd(update: Update, context: ContextTypes.DEFAULT_TYPE):
    await update.message.reply_text("Выбери как ты сейчас 💙", reply_markup=build_keyboard())


async def handle_mood(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    key = query.data.replace("mood_", "")
    if key not in MOODS:
        return
    back_kb = InlineKeyboardMarkup([[
        InlineKeyboardButton("◀️ Назад к настроениям", callback_data="back")
    ]])
    await query.message.reply_text(build_response(MOODS[key]), reply_markup=back_kb)


async def back(update: Update, context: ContextTypes.DEFAULT_TYPE):
    query = update.callback_query
    await query.answer()
    await query.message.reply_text("Как ты сейчас? 💙", reply_markup=build_keyboard())


def main():
    app = Application.builder().token(BOT_TOKEN).build()
    app.add_handler(CommandHandler("start", start))
    app.add_handler(CommandHandler("menu",  menu_cmd))
    app.add_handler(CallbackQueryHandler(handle_mood, pattern="^mood_"))
    app.add_handler(CallbackQueryHandler(back,        pattern="^back$"))
    logger.info("✅  Бот запущен")
    app.run_polling(allowed_updates=Update.ALL_TYPES)


if __name__ == "__main__":
    main()